"""Generate reproducible CartPole/Acrobot swing-up experiment manifests.

The module deliberately *prints* training commands; it never launches them.  A
Slurm array can therefore select exactly one stable, zero-based manifest row::

    python -m benchmarks.swingup_final_matrix \
        --phase final-core --slurm-index

The emitted commands target configuration rows in
``benchmarks/hyperparams/ct_sac.csv``.  The final study expects these rows for
both ``cartpole-swingup`` and ``acrobot-swingup-v2``::

    final_mf, final_mf_vhead, final_oracle_rollout,
    final_structured, final_guarded, final_reanchor,
    final_guarded_reanchor

The backend gate additionally expects ``final_oracle_loop``.  Acrobot tuning
uses mode names generated by :func:`acrobot_tuning_mode`; its manifest includes
the corresponding gamma, initial log standard deviation, and learning rate so
the CSV rows can be generated or audited without decoding names by hand.

Primary phase sizes are fixed:

* ``backend-gate``: 2 environments x 2 backends x 3 seeds = 12 jobs.
* ``acrobot-tuning``: 3 gammas x 2 log stds x 2 learning rates x 4 seeds
  = 48 jobs.
* ``final-core``: 2 environments x 6 modes x 12 seeds = 144 jobs.
* ``final-combined``: optional guard + re-anchor interaction, 24 jobs.
"""

from __future__ import annotations

import argparse
import csv
import json
import os
from pathlib import Path
import shlex
import sys
from dataclasses import asdict, dataclass, replace
from typing import Iterable, Sequence


CARTPOLE_ENV = "cartpole-swingup"
ACROBOT_V2_ENV = "acrobot-swingup-v2"

FINAL_MODES = (
    "final_mf",
    "final_mf_vhead",
    "final_oracle_rollout",
    "final_structured",
    "final_guarded",
    "final_reanchor",
)

BACKEND_MODES = (
    "final_oracle_loop",
    "final_oracle_rollout",
)

COMBINED_MODE = "final_guarded_reanchor"

PHASES = (
    "backend-gate",
    "acrobot-tuning",
    "final-core",
    "final-combined",
)
PHASE_COUNTS = {
    "backend-gate": 12,
    "acrobot-tuning": 48,
    "final-core": 144,
    "final-combined": 24,
    "all": 228,
}

DEFAULT_RUN_IDS = {
    "backend-gate": "swingup_backend_gate_v1",
    "acrobot-tuning": "acrobot_v2_tuning_v1",
    "final-core": "swingup_final_v1",
    "final-combined": "swingup_final_v1",
}


@dataclass(frozen=True)
class MatrixJob:
    """One stable row of an experiment array."""

    array_index: int
    phase: str
    env_id: str
    mode: str
    seed: int
    total_timesteps: int
    eval_mode: str
    run_id: str
    cpus: int
    gamma: float | None = None
    log_std_init: float | None = None
    learning_rate: float | None = None


def acrobot_tuning_mode(
    gamma: float, log_std_init: float, learning_rate: float
) -> str:
    """Return the canonical CSV mode name for one Acrobot tuning cell."""

    gamma_tokens = {0.99: "g0990", 0.995: "g0995", 0.999: "g0999"}
    log_std_tokens = {-0.5: "ls_m0p5", -1.0: "ls_m1p0"}
    learning_rate_tokens = {3e-4: "lr_3e4", 7.3e-4: "lr_7p3e4"}
    try:
        gamma_token = gamma_tokens[float(gamma)]
        log_std_token = log_std_tokens[float(log_std_init)]
        learning_rate_token = learning_rate_tokens[float(learning_rate)]
    except KeyError as exc:
        raise ValueError(
            "unsupported Acrobot tuning cell: "
            f"gamma={gamma}, log_std_init={log_std_init}, "
            f"learning_rate={learning_rate}"
        ) from exc
    return f"acrov2_tune_{gamma_token}_{log_std_token}_{learning_rate_token}"


def _backend_gate_jobs() -> list[MatrixJob]:
    jobs: list[MatrixJob] = []
    for env_id in (CARTPOLE_ENV, ACROBOT_V2_ENV):
        for mode in BACKEND_MODES:
            for seed in (90, 91, 92):
                jobs.append(
                    MatrixJob(
                        array_index=len(jobs),
                        phase="backend-gate",
                        env_id=env_id,
                        mode=mode,
                        seed=seed,
                        total_timesteps=100_000,
                        eval_mode="final_mf",
                        run_id=DEFAULT_RUN_IDS["backend-gate"],
                        # Allocate the same resources to loop and rollout so
                        # elapsed-time comparisons measure the backend choice.
                        cpus=4,
                    )
                )
    return jobs


def _acrobot_tuning_jobs() -> list[MatrixJob]:
    jobs: list[MatrixJob] = []
    for gamma in (0.99, 0.995, 0.999):
        for log_std_init in (-0.5, -1.0):
            for learning_rate in (3e-4, 7.3e-4):
                mode = acrobot_tuning_mode(
                    gamma, log_std_init, learning_rate
                )
                for seed in (100, 101, 102, 103):
                    jobs.append(
                        MatrixJob(
                            array_index=len(jobs),
                            phase="acrobot-tuning",
                            env_id=ACROBOT_V2_ENV,
                            mode=mode,
                            seed=seed,
                            total_timesteps=500_000,
                            eval_mode="final_mf",
                            run_id=DEFAULT_RUN_IDS["acrobot-tuning"],
                            cpus=1,
                            gamma=gamma,
                            log_std_init=log_std_init,
                            learning_rate=learning_rate,
                        )
                    )
    return jobs


def _final_core_jobs() -> list[MatrixJob]:
    jobs: list[MatrixJob] = []
    for env_id, total_timesteps in (
        (CARTPOLE_ENV, 500_000),
        (ACROBOT_V2_ENV, 1_000_000),
    ):
        for mode in FINAL_MODES:
            for seed in range(12):
                jobs.append(
                    MatrixJob(
                        array_index=len(jobs),
                        phase="final-core",
                        env_id=env_id,
                        mode=mode,
                        seed=seed,
                        total_timesteps=total_timesteps,
                        eval_mode="final_mf",
                        run_id=DEFAULT_RUN_IDS["final-core"],
                        cpus=4,
                    )
                )
    return jobs


def _final_combined_jobs() -> list[MatrixJob]:
    jobs: list[MatrixJob] = []
    for env_id, total_timesteps in (
        (CARTPOLE_ENV, 500_000),
        (ACROBOT_V2_ENV, 1_000_000),
    ):
        for seed in range(12):
            jobs.append(
                MatrixJob(
                    array_index=len(jobs),
                    phase="final-combined",
                    env_id=env_id,
                    mode=COMBINED_MODE,
                    seed=seed,
                    total_timesteps=total_timesteps,
                    eval_mode="final_mf",
                    run_id=DEFAULT_RUN_IDS["final-combined"],
                    cpus=4,
                )
            )
    return jobs


def build_jobs(phase: str) -> list[MatrixJob]:
    """Build a phase manifest with contiguous zero-based indices."""

    builders = {
        "backend-gate": _backend_gate_jobs,
        "acrobot-tuning": _acrobot_tuning_jobs,
        "final-core": _final_core_jobs,
        "final-combined": _final_combined_jobs,
    }
    if phase == "all":
        combined: list[MatrixJob] = []
        for phase_name in PHASES:
            for job in builders[phase_name]():
                combined.append(replace(job, array_index=len(combined)))
        jobs = combined
    else:
        try:
            jobs = builders[phase]()
        except KeyError as exc:
            raise ValueError(f"unknown phase {phase!r}") from exc

    expected = PHASE_COUNTS[phase]
    if len(jobs) != expected:
        raise AssertionError(
            f"internal matrix error: {phase} produced {len(jobs)} jobs, "
            f"expected {expected}"
        )
    identities = {(job.phase, job.env_id, job.mode, job.seed) for job in jobs}
    if len(identities) != len(jobs):
        raise AssertionError(f"internal matrix error: duplicate jobs in {phase}")
    return jobs


def command_for_job(
    job: MatrixJob,
    *,
    python: str = "python",
    hyperparams_dir: str = "benchmarks/hyperparams",
    log_root: str = "logs",
    save_root: str = "saved_models",
    n_eval_episodes: int = 10,
    run_id: str | None = None,
    desc: str = "",
    max_seconds: float | None = None,
    resume: bool = False,
) -> str:
    """Render one job as a safely shell-quoted runner command."""

    selected_run_id = run_id or job.run_id
    argv = [
        python,
        "-m",
        "benchmarks.run_ct_rl",
        "--algos",
        "ct_sac",
        "--env_id",
        job.env_id,
        "--mode",
        job.mode,
        "--eval_mode",
        job.eval_mode,
        "--seed",
        str(job.seed),
        "--total_timesteps",
        str(job.total_timesteps),
        "--n_eval_episodes",
        str(n_eval_episodes),
        "--hyperparams_dir",
        hyperparams_dir,
        "--log_root",
        log_root,
        "--save_root",
        save_root,
        "--run_id",
        selected_run_id,
    ]
    if desc:
        argv.extend(("--desc", desc))
    if max_seconds is not None:
        argv.extend(("--max_seconds", str(max_seconds)))
    if resume:
        argv.append("--resume")
    return shlex.join(argv)


def _safe_path_token(value: str) -> str:
    return "".join(
        character if (character.isalnum() or character in "-_") else "_"
        for character in str(value)
    )


def expected_checkpoint_path(
    job: MatrixJob,
    *,
    save_root: str,
    run_id: str | None = None,
    desc: str = "",
) -> Path:
    """Return the exact endpoint path produced by :mod:`benchmarks.run_ct_rl`."""

    parts = ["dt_0_01", "maxs_2000"]
    if desc:
        parts.append(_safe_path_token(desc))
    parts.append(_safe_path_token(run_id or job.run_id))
    return (
        Path(save_root).expanduser().resolve()
        / "ct_sac"
        / job.env_id
        / job.mode
        / f"seed_{job.seed}"
        / "_".join(parts)
        / "final_model.pth"
    )


def _record_for_job(
    job: MatrixJob,
    command: str,
    *,
    save_root: str,
    run_id: str | None,
    desc: str,
) -> dict[str, object]:
    record = asdict(job)
    record["train_seed"] = job.seed
    record["checkpoint_path"] = str(
        expected_checkpoint_path(
            job, save_root=save_root, run_id=run_id, desc=desc
        )
    )
    record["command"] = command
    return record


def _emit_csv(records: Iterable[dict[str, object]]) -> None:
    records = list(records)
    if not records:
        return
    writer = csv.DictWriter(sys.stdout, fieldnames=list(records[0]))
    writer.writeheader()
    writer.writerows(records)


def _build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        description=(
            "Emit deterministic commands or manifests for the swing-up backend "
            "gate, Acrobot-v2 tuning grid, 144-run core comparison, and "
            "optional 24-run combined arm. "
            "Array indices are zero-based."
        )
    )
    parser.add_argument(
        "--phase",
        choices=(*PHASES, "all"),
        default="final-core",
        help="Matrix phase to emit (default: final-core).",
    )
    parser.add_argument(
        "--format",
        choices=("commands", "csv", "json"),
        default="commands",
        help="Output format (default: one shell command per line).",
    )
    parser.add_argument(
        "--index",
        type=int,
        default=None,
        help="Emit only this zero-based array row.",
    )
    parser.add_argument(
        "--slurm-index",
        action="store_true",
        help="Select the row from SLURM_ARRAY_TASK_ID.",
    )
    parser.add_argument(
        "--count",
        action="store_true",
        help="Print only the number of jobs in the selected phase.",
    )
    parser.add_argument(
        "--list-phases",
        action="store_true",
        help="Print phase names and counts, then exit.",
    )
    parser.add_argument("--python", default="python", help="Python executable.")
    parser.add_argument(
        "--hyperparams-dir", default="benchmarks/hyperparams"
    )
    parser.add_argument("--log-root", default="logs")
    parser.add_argument("--save-root", default="saved_models")
    parser.add_argument("--n-eval-episodes", type=int, default=10)
    parser.add_argument(
        "--run-id",
        default=None,
        help="Override the phase-specific stable run ID.",
    )
    parser.add_argument("--desc", default="")
    parser.add_argument(
        "--max-seconds",
        type=float,
        default=None,
        help="Forward a wall-clock checkpoint budget to run_ct_rl.",
    )
    parser.add_argument(
        "--resume",
        action="store_true",
        help="Forward --resume (use only with a stable, previously started run ID).",
    )
    return parser


def main(argv: Sequence[str] | None = None) -> int:
    parser = _build_parser()
    args = parser.parse_args(argv)

    if args.list_phases:
        for phase_name in (*PHASES, "all"):
            print(f"{phase_name},{PHASE_COUNTS[phase_name]}")
        return 0
    if args.index is not None and args.slurm_index:
        parser.error("--index and --slurm-index are mutually exclusive")
    if args.n_eval_episodes < 1:
        parser.error("--n-eval-episodes must be >= 1")
    if args.max_seconds is not None and args.max_seconds <= 0:
        parser.error("--max-seconds must be > 0")

    jobs = build_jobs(args.phase)
    if args.count:
        print(len(jobs))
        return 0

    selected_index = args.index
    if args.slurm_index:
        raw_index = os.environ.get("SLURM_ARRAY_TASK_ID")
        if raw_index is None:
            parser.error("SLURM_ARRAY_TASK_ID is not set")
        try:
            selected_index = int(raw_index)
        except ValueError:
            parser.error(
                f"SLURM_ARRAY_TASK_ID must be an integer, got {raw_index!r}"
            )
    if selected_index is not None:
        if selected_index < 0 or selected_index >= len(jobs):
            parser.error(
                f"array index {selected_index} is outside [0, {len(jobs) - 1}]"
            )
        jobs = [jobs[selected_index]]

    records = []
    commands = []
    for job in jobs:
        command = command_for_job(
            job,
            python=args.python,
            hyperparams_dir=args.hyperparams_dir,
            log_root=args.log_root,
            save_root=args.save_root,
            n_eval_episodes=args.n_eval_episodes,
            run_id=args.run_id,
            desc=args.desc,
            max_seconds=args.max_seconds,
            resume=args.resume,
        )
        commands.append(command)
        records.append(
            _record_for_job(
                job,
                command,
                save_root=args.save_root,
                run_id=args.run_id,
                desc=args.desc,
            )
        )

    if args.format == "commands":
        print("\n".join(commands))
    elif args.format == "csv":
        _emit_csv(records)
    else:
        json.dump(records, sys.stdout, indent=2, sort_keys=True)
        sys.stdout.write("\n")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
